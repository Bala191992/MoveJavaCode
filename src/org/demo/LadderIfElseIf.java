package org.demo;

//LadderIf or Else if
public class LadderIfElseIf {
	public static void main(String[] args) {
		
		int a = 10;
				
if (a<8) {
	System.out.println("print IF block");
}		
else if (a<9) {
	System.out.println("print else if 1");
}		
		
else if (a==12) {
	System.out.println("print else if 2");
}		
else if (a != 10) {
	System.out.println("print else if 3");
}		

else {
	System.out.println("Print else present");
}
	}
}
