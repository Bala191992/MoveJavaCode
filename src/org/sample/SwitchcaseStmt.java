package org.sample;

public class SwitchcaseStmt {
	
	public static void main(String[] args) {
		
		//String day= "Wed";
		String day= "ABC";
		
		switch (day) {
		case "Mon":
			System.out.println("Monday");
			break;
		case "Thu":
			System.out.println("Monday");
			break;	
		case "Wed":
			System.out.println("Monday");
			break;	
		case "Thus":
			System.out.println("Monday");
			break;
		case "Fri":
			System.out.println("Monday");
			break;
			
			
		default:
			System.out.println("Unknown Details");
			break;
		}
	}
}
